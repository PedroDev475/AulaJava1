public class CastingImplicito {
    public static void main(String[] args) {
        // Declarando a variável do tipo int
        int valorInt = 42;

        // Casting implícito de int para double
        double valorDouble = valorInt;

        // Exibindo o valor da variável double
        System.out.println("Valor int: " + valorInt);
        System.out.println("Valor double (após casting implícito): " + valorDouble);
    }
}
